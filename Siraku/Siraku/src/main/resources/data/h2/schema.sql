CREATE TABLE IF NOT EXISTS SEQUENCES(
NOMBRE VARCHAR(50) PRIMARY KEY,
VALOR BIGINT NOT NULL
);

CREATE TABLE IF NOT EXISTS DEPARTMENT
 (DEPT_NO VARCHAR(3) PRIMARY KEY
 ,DEPT_NAME VARCHAR(36) NOT NULL
 ,MGRNO VARCHAR(6)
 ,ADMR_DEPT VARCHAR(3) NOT NULL
 ,LOCATION VARCHAR(16));
 
 CREATE TABLE IF NOT EXISTS EMPLOYEE
 (EMP_NO VARCHAR(6) PRIMARY KEY
 ,FIRST_NAME VARCHAR(12) NOT NULL
 ,MIDINIT VARCHAR(1)
 ,LAST_NAME VARCHAR(15) NOT NULL
 ,WORK_DEPT VARCHAR(3) REFERENCES DEPARTMENT(DEPT_NO)
 ,PHONE_NO VARCHAR(4)
 ,HIRE_DATE DATE
 ,JOB VARCHAR(8)
 ,ED_LEVEL SMALLINT NOT NULL
 ,SEX VARCHAR(1)
 ,BIRTH_DATE DATE
 ,SALARY FLOAT
 ,BONUS FLOAT
 ,COMM FLOAT);
 
CREATE TABLE IF NOT EXISTS COMERCIALES(
 COD_COMERCIAL BIGINT PRIMARY KEY,
 NOMBRE VARCHAR(20),
 APELLIDO1 VARCHAR(20),
 APELLIDO2 VARCHAR(20)
);
 
 CREATE TABLE IF NOT EXISTS PRODUCTOS(
 COD_PRODUCTO BIGINT PRIMARY KEY,
 NOMBRE VARCHAR(200),
 PRECIO DOUBLE,
 FECHA_ALTA DATE,
 FAMILIA VARCHAR(20),
 DESCATALOGADO BOOLEAN,
 COD_COMERCIAL BIGINT,
 
 CONSTRAINT PRO1_FK FOREIGN KEY (COD_COMERCIAL) REFERENCES COMERCIALES(COD_COMERCIAL)
 );
 
 CREATE TABLE IF NOT EXISTS CLIENTES(
 IDENTIFICADOR_FISCAL VARCHAR(200) PRIMARY KEY,
 NOMBRE_COMERCIAL VARCHAR(80),
 NOMBRE VARCHAR(20),
 APELLIDO1 VARCHAR(20),
 APELLIDO2 VARCHAR(20),
 
 DIRECCION VARCHAR(300),
 POBLACION VARCHAR(30),
 COD_POSTAL VARCHAR(20),
 PROVINCIA VARCHAR(50),
 PAIS VARCHAR(50),
 
 TELEFONO1 VARCHAR(9),
 TELEFONO2 VARCHAR(9),
 EMAIL VARCHAR(50)
 );
 
 CREATE TABLE IF NOT EXISTS PEDIDOS(
 IDENTIFICADOR_FISCAL VARCHAR(200),
 COD_PRODUCTO BIGINT,
 
 CONSTRAINT PED_PK PRIMARY KEY (IDENTIFICADOR_FISCAL,COD_PRODUCTO),
 CONSTRAINT PED1_FK FOREIGN KEY (IDENTIFICADOR_FISCAL) REFERENCES CLIENTES(IDENTIFICADOR_FISCAL),
 CONSTRAINT PED2_FK FOREIGN KEY (COD_PRODUCTO) REFERENCES PRODUCTOS(COD_PRODUCTO)
 );